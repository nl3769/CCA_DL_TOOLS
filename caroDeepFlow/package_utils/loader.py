from mat4py import loadmat
import imageio as iio
import nibabel as nib

# ----------------------------------------------------------------------------------------------------------------------
def load_mat(fname: str):
    """Load .mat file. """

    data = loadmat(fname)

    return data

# ----------------------------------------------------------------------------------------------------------------------
def load_image(fname: str):
    """Load image. """

    I = iio.imread(fname)

    return I

# ----------------------------------------------------------------------------------------------------------------------
def load_nii(fname: str):
    """Load .nii file. """

    data = nib.load(fname)

    return data

# ----------------------------------------------------------------------------------------------------------------------